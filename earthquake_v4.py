    mainshock_time = cluster_df.loc[mainshock_idx, 'time']
    mainshock_mag = cluster_df.loc[mainshock_idx, 'mag']

    # --- Use only events BEFORE mainshock ---
    pre_df = cluster_df[cluster_df['time'] < mainshock_time]

    if len(pre_df) < window_size:
        return None

    # --- Get Mc ---
    Mc = stats_df.loc[stats_df['Cluster_ID'] == cluster_id, 'Mc_used'].values[0]

    mags = pre_df['mag'].values
    times = pre_df['time'].values

    b_values = []
    b_times = []

    for i in range(len(mags) - window_size + 1):

        segment = mags[i:i+window_size]
        segment = segment[segment >= Mc]

        if len(segment) < 10:
            continue

        mean_mag = np.mean(segment)
        b_val = np.log10(np.e) / (mean_mag - (Mc - 0.05))

        b_values.append(b_val)
        b_times.append(times[i + window_size - 1])

    if len(b_values) < 5:
        return None

    b_values = np.array(b_values)

    baseline_mean = np.mean(b_values[:int(len(b_values)*0.5)])
    baseline_std = np.std(b_values[:int(len(b_values)*0.5)])

    min_b = np.min(b_values)
    min_time = b_times[np.argmin(b_values)]

    z_score = (baseline_mean - min_b) / baseline_std if baseline_std > 0 else np.nan

    return {
        'Cluster_ID': cluster_id,
        'Mainshock_Mag': mainshock_mag,
        'Min_b_before_mainshock': min_b,
        'Baseline_b_mean': baseline_mean,
        'Z_score_drop': z_score,
        'Anomaly_time': min_time,
        'Mainshock_time': mainshock_time
    }

