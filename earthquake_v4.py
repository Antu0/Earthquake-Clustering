def plot_zoomed_cross_section(df, start_coord, end_coord, width_km=20):
    """
    Dynamically zooms into a defined line and plots a depth cross-section.
    Forces the depth scatter plot to be vertically taller than the line map.
    """
    lon1, lat1 = start_coord
    lon2, lat2 = end_coord

    # 1. LOCAL CARTESIAN PROJECTION
    mean_lat = np.radians((lat1 + lat2) / 2)
    deg2km_lat = 111.32
    deg2km_lon = 111.32 * np.cos(mean_lat)

    x0, y0 = lon1 * deg2km_lon, lat1 * deg2km_lat
    x1, y1 = lon2 * deg2km_lon, lat2 * deg2km_lat

    vec_line = np.array([x1 - x0, y1 - y0])
    line_len = np.linalg.norm(vec_line)
    vec_line_unit = vec_line / line_len
    vec_line_perp = np.array([-vec_line_unit[1], vec_line_unit[0]])

    # 2. PROJECT ALL EARTHQUAKES ONTO THE LINE
    pts_x = df['longitude'].values * deg2km_lon
    pts_y = df['latitude'].values * deg2km_lat
    vec_pts_x = pts_x - x0
    vec_pts_y = pts_y - y0

    dist_along = vec_pts_x * vec_line_unit[0] + vec_pts_y * vec_line_unit[1]
    dist_perp = vec_pts_x * vec_line_perp[0] + vec_pts_y * vec_line_perp[1]

    # 3. FILTER DATA WITHIN THE SWATH
    mask = (dist_along >= 0) & (dist_along <= line_len) & (np.abs(dist_perp) <= width_km / 2)
    df_sub = df[mask].copy()
    df_sub['dist_along'] = dist_along[mask]

    # 4. FIGURE SETUP
    fig = plt.figure(figsize=(3.54, 6))

    # FIX: Explicitly assign the map to a short, wide box at the very top
    ax_map = fig.add_axes([0.15, 0.65, 0.75, 0.3], projection=ccrs.PlateCarree())
    ax_cs = fig.add_axes([0.15, 0.1, 0.75, 0.45]) # Placeholder, will be adjusted

    custom_colors = ['darkorange','darkblue', 'forestgreen', 'crimson', 'purple', 'saddlebrown', 'teal']
    custom_cmap = mcolors.ListedColormap(custom_colors)

    # --- TOP: MAP VIEW ---
    center_lon = (lon1 + lon2) / 2
    center_lat = (lat1 + lat2) / 2

    buffer_deg = max((width_km / 2) / deg2km_lat, 0.5)
    base_span_lon = abs(lon1 - lon2) + 2 * buffer_deg
    base_span_lat = abs(lat1 - lat2) + 2 * buffer_deg

    # FIX: Calculate Target Aspect Ratio to trick Cartopy into staying short
    # Map width on paper = 0.75 * 3.54 = 2.655 inches
    # Map height on paper = 0.3 * 6 = 1.8 inches
    target_aspect = 2.655 / 1.8

    if (base_span_lon / base_span_lat) < target_aspect:
        span_lon = base_span_lat * target_aspect
        span_lat = base_span_lat
    else:
        span_lon = base_span_lon
        span_lat = base_span_lon / target_aspect

    ax_map.set_extent([center_lon - span_lon / 2, center_lon + span_lon / 2,
                       center_lat - span_lat / 2, center_lat + span_lat / 2],
                      crs=ccrs.PlateCarree())

    ax_map.add_feature(cfeature.LAND, facecolor='#EAEAEA')
    ax_map.add_feature(cfeature.BORDERS, linestyle=':', linewidth=0.5)

    ax_map.scatter(df['longitude'], df['latitude'], c='lightgrey', s=1, alpha=0.3, transform=ccrs.PlateCarree())

    noise_map = df_sub[df_sub['Cluster'] == -1]
    clusters_map = df_sub[df_sub['Cluster'] != -1]

    ax_map.scatter(noise_map['longitude'], noise_map['latitude'], c='grey', s=3, alpha=0.5, transform=ccrs.PlateCarree())
    sc = ax_map.scatter(clusters_map['longitude'], clusters_map['latitude'],
                        c=clusters_map['Cluster'], cmap=custom_cmap, s=8, alpha=0.9,
                        edgecolors='black', linewidth=0.1, transform=ccrs.PlateCarree())

    ax_map.plot([lon1, lon2], [lat1, lat2], color='black', linewidth=1.5, transform=ccrs.PlateCarree())
    ax_map.text(lon1, lat1, 'A ', color='black', fontweight='bold', transform=ccrs.PlateCarree(), ha='right', va='center')
    ax_map.text(lon2, lat2, " A'", color='black', fontweight='bold', transform=ccrs.PlateCarree(), ha='left', va='center')

    box_x = [x0 + vec_line_perp[0]*width_km/2, x0 - vec_line_perp[0]*width_km/2,
             x1 - vec_line_perp[0]*width_km/2, x1 + vec_line_perp[0]*width_km/2, x0 + vec_line_perp[0]*width_km/2]
    box_y = [y0 + vec_line_perp[1]*width_km/2, y0 - vec_line_perp[1]*width_km/2,
             y1 - vec_line_perp[1]*width_km/2, y1 + vec_line_perp[1]*width_km/2, y0 + vec_line_perp[1]*width_km/2]
    box_lon = [x / deg2km_lon for x in box_x]
    box_lat = [y / deg2km_lat for y in box_y]
    ax_map.plot(box_lon, box_lat, 'k--', linewidth=0.8, alpha=0.6, transform=ccrs.PlateCarree())

    gl = ax_map.gridlines(draw_labels=True, color='none')
    gl.top_labels = False
    gl.right_labels = False
    gl.xlabel_style = {'size': 6}
    gl.ylabel_style = {'size': 6}

    # --- BOTTOM: CROSS SECTION ---
    noise_cs = df_sub[df_sub['Cluster'] == -1]
    clusters_cs = df_sub[df_sub['Cluster'] != -1]

    ax_cs.scatter(noise_cs['dist_along'], noise_cs['depth'], c='grey', s=3, alpha=0.5)
    ax_cs.scatter(clusters_cs['dist_along'], clusters_cs['depth'],
                  c=clusters_cs['Cluster'], cmap=custom_cmap, s=8, alpha=0.9, edgecolors='black', linewidth=0.1)

    ax_cs.set_xlim(0, line_len)

    if not df_sub.empty:
        max_depth = df_sub['depth'].max()
        ax_cs.set_ylim(max_depth + 5, -2)
    else:
        ax_cs.set_ylim(50, -2)

    ax_cs.set_xlabel("Distance along profile A-A' (km)", fontsize=8)
    ax_cs.set_ylabel("Depth (km)", fontsize=8)
    ax_cs.grid(False)

    # --- ALIGNMENT MAGIC ---
    fig.canvas.draw()
    map_pos = ax_map.get_position()

    # Dynamically fill all remaining space beneath the map for the cross-section
    gap = 0.05
    cs_bottom = 0.08 # Leave room for the X-axis label at the bottom edge
    cs_height = map_pos.y0 - gap - cs_bottom

    ax_cs.set_position([map_pos.x0, cs_bottom, map_pos.width, cs_height])

    return fig, ax_map, ax_cs

