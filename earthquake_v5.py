# --- 2. GENERATE 3D CURVED FAULT LINES ---
for cluster_id in valid_clusters:
    cluster_data = df[df['Cluster'] == cluster_id]

    if len(cluster_data) < 3:
        continue # Need at least 3 points for a curve

    # Extract 3D coordinates
    lons = cluster_data['longitude'].values
    lats = cluster_data['latitude'].values
    depths = cluster_data['depth'].values

    # Step A: Use 2D PCA to find the primary map-view strike direction
    pca = PCA(n_components=2).fit(np.column_stack((lons, lats)))
    is_vertical = abs(pca.components_[0][1]) > abs(pca.components_[0][0])

    # Step B: Select independent variable to prevent near-infinite slopes
    x_reg = lats if is_vertical else lons
    y_reg = lons if is_vertical else lats
    z_reg = depths # Dependent variable for depth

    # Step C: Fit 2nd-Degree Polynomials for both Geography and Depth
    poly = PolynomialFeatures(degree=2)
    X_poly = poly.fit_transform(x_reg.reshape(-1, 1))

    model_xy = LinearRegression().fit(X_poly, y_reg)
    model_z = LinearRegression().fit(X_poly, z_reg) # New model for depth

    # Step D: Generate smooth points
    x_range = np.linspace(x_reg.min(), x_reg.max(), 50).reshape(-1, 1)
    X_range_poly = poly.transform(x_range)

    y_pred = model_xy.predict(X_range_poly)
    z_pred = model_z.predict(X_range_poly) # Predict depth curve

    # Step E: Recombine into 3D (Longitude, Latitude, Depth) arrays
    if is_vertical:
        line_points = np.column_stack((y_pred, x_range.flatten(), z_pred))
    else:
        line_points = np.column_stack((x_range.flatten(), y_pred, z_pred))

    # Step F: Convert to a 3D Shapely spatial object
    # Shapely seamlessly accepts (x, y, z) tuples
    fault_line = LineString(line_points)

    cluster_stats = stats_df[stats_df['Cluster_ID'] == cluster_id].iloc[0]

    fault_features.append({
        'Cluster_ID': cluster_id,
        'b_value': cluster_stats['b_value'],
        'b_error': cluster_stats['b_error'],
        'Dip_Angle': cluster_stats['Dip_Angle'],
        'geometry': fault_line
    })

