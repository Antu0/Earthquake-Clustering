# --- 1. Define the Optimized b-value function ---
def calculate_b_value_advanced(magnitudes, delta_m=0.1, min_events=MIN_EVENTS_FOR_B):
    """
    Calculates Mc, b-value, and standard error using MAXC+0.2 and MLE.
    """
    magnitudes = np.round(magnitudes, 1)
    counts = magnitudes.value_counts().sort_index()

    if len(counts) == 0:
        return np.nan, np.nan, np.nan

    # Automatic Mc Estimation: MAXC + Correction
    maxc = counts.idxmax()
    mc = np.round(maxc + 0.2, 1)

    # Filter Data
    valid_mags = magnitudes[magnitudes >= mc]
    N = len(valid_mags)

    # Safety Check
    if N < min_events:
        return np.nan, mc, np.nan

    # Calculate b-value (Aki-Utsu Maximum Likelihood Estimation)
    avg_mag = np.mean(valid_mags)
    b_val = np.log10(np.e) / (avg_mag - (mc - (delta_m / 2)))

    # Calculate Standard Error (Shi & Bolt, 1982)
    b_err = b_val / np.sqrt(N)

    return b_val, mc, b_err

