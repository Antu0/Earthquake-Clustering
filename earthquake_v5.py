# --- DEFINE RANGES TO TEST ---
# Notice we dropped eps_range entirely.
alpha_range = [1, 2, 3, 4, 5]
min_cluster_size_range = [10, 15, 20, 25, 30]

# Generate combinations
param_grid = list(itertools.product(alpha_range, min_cluster_size_range))

print(f"Total HDBSCAN combinations to test: {len(param_grid)}")

results = []
total_points = len(df)

print("Running HDBSCAN Grid Search...")

for alpha, min_size in param_grid:

    # 1. Prepare data (Scale depth)
    temp_df = df.copy()
    temp_df["depth_weighted"] = temp_df["depth"] * alpha
    X = temp_df[["X_km", "Y_km", "depth_weighted"]].values

    # 2. Run HDBSCAN
    # We use min_cluster_size as the primary parameter.
    model = HDBSCAN(min_cluster_size=min_size)
    labels = model.fit_predict(X)

    # 3. Metrics
    n_clusters = len(set(labels)) - (1 if -1 in labels else 0)
    n_noise = np.sum(labels == -1)
    noise_pct = (n_noise / total_points) * 100

    if n_clusters > 1:
        mask = labels != -1
        try:
            sil = silhouette_score(X[mask], labels[mask])
        except:
            sil = np.nan
    else:
        sil = np.nan

    results.append({
        "alpha": alpha,
        "min_cluster_size": min_size,
        "n_clusters": n_clusters,
        "n_noise": n_noise,
        "noise_pct": noise_pct,
        "silhouette": sil
    })

res_df = pd.DataFrame(results)
print("Grid Search Complete.")

# Filter out models with excessive noise (e.g., > 50%)
